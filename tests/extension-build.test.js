'use strict';

const test = require('node:test');
const assert = require('node:assert/strict');
const { spawnSync } = require('node:child_process');
const fs = require('node:fs');
const path = require('node:path');
const vm = require('node:vm');

const root = path.resolve(__dirname, '..');
const generatedExtensionRoot = 'skills/browser-control/extension';

function extensionPath(file) {
  return path.posix.join(generatedExtensionRoot, file);
}

function read(relativePath) {
  return fs.readFileSync(path.join(root, relativePath), 'utf8');
}

test('extension build emits declared Chrome-loadable output from src/extension', () => {
  const result = spawnSync(process.execPath, [path.join(root, 'scripts', 'build-extension.mjs')], {
    cwd: root,
    encoding: 'utf8'
  });
  assert.equal(result.status, 0, result.stderr || result.stdout);
  assert.match(result.stdout, /extension build ok/);

  for (const relativePath of [
    extensionPath('manifest.json'),
    extensionPath('service-worker.js'),
    extensionPath('content.js'),
    extensionPath('popup.html'),
    extensionPath('popup.js'),
    extensionPath('options.html'),
    extensionPath('options.js'),
    extensionPath('icons/icon16.png'),
    extensionPath('icons/icon48.png'),
    extensionPath('icons/icon128.png')
  ]) {
    assert.equal(fs.existsSync(path.join(root, relativePath)), true, `${relativePath} should exist`);
  }
});

test('generated extension manifest preserves source manifest semantics', () => {
  const source = JSON.parse(read('src/extension/manifest.json'));
  const generated = JSON.parse(read(extensionPath('manifest.json')));
  assert.deepEqual(generated, source);
  assert.equal(generated.background.service_worker, 'service-worker.js');
  assert.deepEqual(generated.content_scripts[0].js, ['content.js']);
  assert.equal(generated.action.default_popup, 'popup.html');
  assert.equal(generated.options_page, 'options.html');
});

test('generated runtime entrypoints remain classic scripts without runtime module imports', () => {
  for (const relativePath of [
    extensionPath('service-worker.js'),
    extensionPath('content.js'),
    extensionPath('popup.js'),
    extensionPath('options.js')
  ]) {
    const source = read(relativePath);
    const withoutComments = source.replace(/\/\*[\s\S]*?\*\//g, '').replace(/^\s*\/\/.*$/gm, '');
    assert.doesNotMatch(withoutComments, /^\s*import\s/m, relativePath);
    assert.doesNotMatch(withoutComments, /^\s*export\s/m, relativePath);
    assert.doesNotMatch(withoutComments, /\bimport\s*\(/, relativePath);
    assert.match(source, /Generated by scripts\/build-extension\.mjs from src\/extension/);
  }
});


