#!/usr/bin/env node
import { build } from 'esbuild';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outFile = path.join(root, 'skills', 'browser-control', 'scripts', 'protocol.js');

fs.mkdirSync(path.dirname(outFile), { recursive: true });

await build({
  entryPoints: [path.join(root, 'src', 'protocol.ts')],
  outfile: outFile,
  bundle: true,
  platform: 'node',
  target: 'node18',
  format: 'cjs',
  external: [
    'node:*'
  ],
  legalComments: 'none',
  logLevel: 'silent',
  banner: {
    js: [
      "'use strict';",
      '// Generated by scripts/build-protocol.mjs from src/protocol.ts.',
      '// Do not edit this file directly; edit src/protocol.ts and run npm run build:protocol.'
    ].join('\n')
  }
});

console.log(`protocol build ok: bundled ${path.relative(root, outFile)}`);
