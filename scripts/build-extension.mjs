#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import * as esbuild from 'esbuild';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const srcRoot = path.join(root, 'src', 'extension');
const skillRoot = path.join(root, 'skills', 'browser-control');
const skillOutRoot = path.join(skillRoot, 'extension');
const wsSourceRoot = path.join(root, 'node_modules', 'ws');
const wsVendorRoot = path.join(skillRoot, 'scripts', 'vendor', 'ws');
const packageJson = JSON.parse(fs.readFileSync(path.join(root, 'package.json'), 'utf8'));
const packageVersion = packageJson.version;

const banner = [
  '// Generated by scripts/build-extension.mjs from src/extension/**.',
  '// Do not edit this file directly; edit src/extension and run npm run build:extension.'
].join('\n');

const scriptOutputs = [
  { out: 'service-worker.js', entry: 'service-worker/index.ts' },
  { out: 'popup.js', entry: 'ui/popup.ts' },
  { out: 'options.js', entry: 'ui/options.ts' }
];

const staticCopies = [
  ['manifest.json', 'manifest.json'],
  ['pages/popup.html', 'popup.html'],
  ['pages/options.html', 'options.html'],
  ['assets/icons/icon16.png', 'icons/icon16.png'],
  ['assets/icons/icon48.png', 'icons/icon48.png'],
  ['assets/icons/icon128.png', 'icons/icon128.png']
];

const declaredOutputs = new Set([
  ...scriptOutputs.map(item => item.out),
  ...staticCopies.map(([, out]) => out)
]);

const vendorWsFiles = [
  'LICENSE',
  'README.md',
  'browser.js',
  'index.js',
  'lib/buffer-util.js',
  'lib/constants.js',
  'lib/event-target.js',
  'lib/extension.js',
  'lib/limiter.js',
  'lib/permessage-deflate.js',
  'lib/receiver.js',
  'lib/sender.js',
  'lib/stream.js',
  'lib/subprotocol.js',
  'lib/validation.js',
  'lib/websocket-server.js',
  'lib/websocket.js',
  'package.json',
  'wrapper.mjs'
];

function ensureParent(file) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
}

function assertDeclared(relativePath) {
  if (!declaredOutputs.has(relativePath)) {
    throw new Error(`Refusing to write undeclared extension output: ${relativePath}`);
  }
}

function copyOutput(outRoot, srcRelative, outRelative) {
  assertDeclared(outRelative);
  const src = path.join(srcRoot, srcRelative);
  const out = path.join(outRoot, outRelative);
  if (!fs.existsSync(src)) throw new Error(`Missing extension static input: ${srcRelative}`);
  ensureParent(out);
  if (srcRelative === 'manifest.json' && outRelative === 'manifest.json') {
    const manifest = JSON.parse(fs.readFileSync(src, 'utf8'));
    if (!isChromeExtensionVersion(packageVersion)) {
      throw new Error(`package.json version ${JSON.stringify(packageVersion)} is not a valid Chrome extension manifest version`);
    }
    manifest.version = packageVersion;
    fs.writeFileSync(out, `${JSON.stringify(manifest, null, 2)}\n`);
    return;
  }
  fs.copyFileSync(src, out);
}

function isChromeExtensionVersion(version) {
  return typeof version === 'string' && /^(?:0|[1-9]\d*)(?:\.(?:0|[1-9]\d*)){0,3}$/.test(version);
}

function assertClassicRuntime(relativePath, content) {
  const withoutComments = content.replace(/\/\*[\s\S]*?\*\//g, '').replace(/^\s*\/\/.*$/gm, '');
  if (/^\s*import\s/m.test(withoutComments) || /^\s*export\s/m.test(withoutComments)) {
    throw new Error(`${relativePath} contains top-level import/export; generated extension entrypoints must be classic scripts.`);
  }
  if (/\bimport\s*\(/.test(withoutComments)) {
    throw new Error(`${relativePath} contains dynamic import(); MV3 generated runtime contract forbids it.`);
  }
}

function buildExtensionTo(outRoot) {
  fs.mkdirSync(outRoot, { recursive: true });

  for (const item of scriptOutputs) {
    assertDeclared(item.out);
    const entry = path.join(srcRoot, item.entry);
    const outfile = path.join(outRoot, item.out);
    if (!fs.existsSync(entry)) throw new Error(`Missing extension script entry: ${item.entry}`);
    ensureParent(outfile);
    esbuild.buildSync({
      entryPoints: [entry],
      outfile,
      bundle: true,
      format: 'iife',
      platform: 'browser',
      target: 'es2022',
      minify: false,
      sourcemap: false,
      treeShaking: false,
      legalComments: 'none',
      logLevel: 'silent',
      banner: { js: banner }
    });
    assertClassicRuntime(item.out, fs.readFileSync(outfile, 'utf8'));
  }

  for (const [src, out] of staticCopies) {
    copyOutput(outRoot, src, out);
  }
}

function syncVendoredWs() {
  if (!fs.existsSync(path.join(wsSourceRoot, 'package.json'))) {
    throw new Error('Missing node_modules/ws. Run npm install before building the self-contained skill package.');
  }
  fs.rmSync(wsVendorRoot, { recursive: true, force: true });
  for (const relativePath of vendorWsFiles) {
    const src = path.join(wsSourceRoot, relativePath);
    const out = path.join(wsVendorRoot, relativePath);
    if (!fs.existsSync(src)) throw new Error(`Missing ws vendor input: ${relativePath}`);
    ensureParent(out);
    fs.copyFileSync(src, out);
  }
}

buildExtensionTo(skillOutRoot);
syncVendoredWs();

console.log(`extension build ok: bundled ${scriptOutputs.length} entries and wrote ${declaredOutputs.size} declared outputs to ${path.relative(root, skillOutRoot)} with manifest version ${packageVersion}; vendored ws into ${path.relative(root, wsVendorRoot)}/`);
